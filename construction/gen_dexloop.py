#!/usr/bin/env python3
"""Emit the dexloop{N} tops — the SLOAD -> swap -> SSTORE COMPOSITION pilot:
the last integration gap in SWAP_DATAPATH.md. Storage (store16x4 pattern) and
the swap program (gen_swap256.py) are each MATCHI-verified alone; this unit
closes the loop: the swap's reserve operands are LOADED from the masked
scratchpad (registered per-lane read port), the swap runs unmodified, and the
new reserves are WRITTEN BACK into the same scratchpad — all in the two-share
domain, all sequencing public.

Composition rules (design record):
  * The swap program body is reused VERBATIM from gen_swap256.py (imported,
    argv-patched): same gadget bodies, same schedule constants, same counter.
    The ONLY change inside the swap text: the held-operand load sources for
    reserveIn/reserveOut are redirected from input ports to the storage-read
    capture registers rri/rro (per-share, same-edge, deterministic string
    replacement asserted unique). x (amountIn) and mo (minOut) stay ports
    (calldata, not storage).
  * Storage lanes are a structural copy of the VERIFIED store16x4: two
    physically-separate lanes m0_*/m1_*, explicit registers (no memory
    inference), per-lane write decode, registered per-lane read port
    (transitions stay in-lane). Paging dropped (verified separately, unused).
  * Prefix FSM (public counter pcnt, cycle 0 = tx_go): SLOAD slot0 -> rri
    (both lanes captured at the SAME edge), SLOAD slot1 -> rro, then the
    internal swap `go` strobe fires at pcnt=={PGO}. WRITEBACK: at swap-counter
    OK_CAP+2 slot0 <= D (newReserveIn), at OK_CAP+3 slot1 <= nror (newRO) —
    per-lane muxed write values (lane k logic sees only share-k values of
    anything), strictly before the global clr at CLR_AT.
  * NEW-GLUE NEGATIVE CONTROL `_sharedbus` (must FAIL): the rri capture is
    routed through ONE time-multiplexed bus register — share 0 of the loaded
    reserve on one edge, share 1 of the SAME word on the next (the store16x4
    sharedbus violation, planted in the NEW hand-off): the bus register's
    value transition combines the shares in the transition model.
  * The swap-core controls come along unchanged: `_pini` label control
    (embedded bubble-free divider, LIVE FAIL), `_rndreuse` (u_dm_1 reuses
    u_dm_0's r/s; the glue itself has ZERO gadgets and ZERO randomness — like
    eq256, the reuse is planted in the embedded core), `_recomb`
    (leak <= aout[0]^aout[1]).

Emits: dexloop{N}.v (target) + _pini/_rndreuse/_recomb/_sharedbus controls,
tb_dexloop{N}.v (MATCHI tb: 2 preload writes, tx_go, windows never close),
tb_smoke_dexloop{N}.v (functional: preload reserves via SSTORE, run swap,
check outputs AND read the written-back reserves out of storage via SLOAD).
Generated by gen_dexloop.py; do not hand-edit — regenerate instead.
Usage: gen_dexloop.py [N]   (default 16; storage D=4 words)
"""
