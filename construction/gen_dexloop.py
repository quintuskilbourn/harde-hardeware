def rep1(text, old, new):
    """Replace exactly-once (composition surgery must be deterministic)."""
    n = text.count(old)
    assert n == 1, f"expected 1 occurrence, got {n}: {old[:60]!r}"
    return text.replace(old, new)


def share_bits(port, share):
    return "{" + ", ".join(f"{port}[{2*j+share}]" for j in range(N - 1, -1, -1)) + "}"


def rmux(lane, addr):
    e = f"m{lane}_{D-1}"
    for i in range(D - 2, -1, -1):
        e = f"(({addr} == {AW}'d{i}) ? m{lane}_{i} : {e})"
    return e


def storage_early(sharedbus=False):
    """Declarations + read path + prefix FSM. Inserted BEFORE the swap
    sequencing text (iverilog needs declaration-before-use; everything that
    references swap regs — cnt, D, nror — lives in storage_late)."""
    PGO = 6 if sharedbus else 4
    L = []
    o = L.append
    o(f"""
// ===== masked storage lanes (store16x4 structural copy; paging dropped) =====
// wdata consumed exactly when we is high; storage (and rdata) sensitive from
// the first write to the end of the trace (sticky window, never closes).
reg seen_w;
always @(posedge clk) begin
    if (rst) seen_w <= 1'b0;
    else if (we) seen_w <= 1'b1;
end
(* keep *) wire a_act      = we;
(* keep *) wire st_out_act = we || seen_w;
""")
    for lane in (0, 1):
        for i in range(D):
            o(f"reg [{N-1}:0] m{lane}_{i};")
    o(f"""
// ---- per-lane external write decode (lane k sees only share-k bits) ----
wire [{N-1}:0] wd0 = {share_bits('wdata', 0)};
wire [{N-1}:0] wd1 = {share_bits('wdata', 1)};

// ---- prefix FSM (public; cycle 0 = tx_go): SLOAD ri, SLOAD ro, swap go ----
reg [2:0] pcnt;
always @(posedge clk) begin
    if (rst)                          pcnt <= 3'd0;
    else if (tx_go)                   pcnt <= 3'd1;
    else if (pcnt != 3'd0 && pcnt != 3'd7) pcnt <= pcnt + 3'd1;
end""")
    if sharedbus:
        o(f"""// sharedbus control: hold slot0 on the read port two cycles, slot1 next
wire [{AW-1}:0] st_raddr = (pcnt == 3'd1 || pcnt == 3'd2) ? {AW}'d0 :
                           (pcnt == 3'd3) ? {AW}'d1 : raddr;""")
    else:
        o(f"""wire [{AW-1}:0] st_raddr = (pcnt == 3'd1) ? {AW}'d0 :
                           (pcnt == 3'd2) ? {AW}'d1 : raddr;""")
    o(f"""
// ---- registered per-lane read port (SLOAD); transitions stay in-lane ----
reg [{N-1}:0] rreg0, rreg1;
always @(posedge clk) begin
    rreg0 <= {rmux(0, 'st_raddr')};
    rreg1 <= {rmux(1, 'st_raddr')};
end""")
    for j in range(N):
        o(f"assign rdata[{2*j}] = rreg0[{j}];  assign rdata[{2*j+1}] = rreg1[{j}];")
    o(f"""
// ---- SLOAD capture registers feeding the swap held-operand loads ----
// (persistent copies of storage words — sticky sensitivity like the pad;
// no clr, mirroring the storage lanes themselves)
reg [{N-1}:0] rri0, rri1, rro0, rro1;""")
    if sharedbus:
        o(f"""// BUG UNDER TEST (sharedbus): ONE time-multiplexed bus register carries
// share 0 of the loaded reserveIn on one edge and share 1 of the SAME word on
// the next — the bus register's value transition combines the two shares
// (transition model). The verified unit captures both lanes at the SAME edge.
reg [{N-1}:0] bus;
always @(posedge clk) begin
    if (rst) begin
        rri0 <= 0; rri1 <= 0; rro0 <= 0; rro1 <= 0; bus <= 0;
    end else begin
        if (pcnt == 3'd2) bus <= rreg0;                      // share 0 of slot0
        if (pcnt == 3'd3) begin rri0 <= bus; bus <= rreg1; end // share0 -> share1 TRANSITION
        if (pcnt == 3'd4) begin
            rri1 <= bus;
            rro0 <= rreg0; rro1 <= rreg1;                    // slot1, same edge
        end
    end
end
wire go = (pcnt == 3'd{PGO});   // internal swap-go strobe""")
    else:
        o(f"""// both lanes captured at the SAME edge (the store16x4 paging rule)
always @(posedge clk) begin
    if (rst) begin
        rri0 <= 0; rri1 <= 0; rro0 <= 0; rro1 <= 0;
    end else begin
        if (pcnt == 3'd2) begin rri0 <= rreg0; rri1 <= rreg1; end   // slot0
        if (pcnt == 3'd3) begin rro0 <= rreg0; rro1 <= rreg1; end   // slot1
    end
end
wire go = (pcnt == 3'd{PGO});   // internal swap-go strobe""")
    return "\n".join(L)


def storage_late():
    """Writeback: references swap regs (cnt, D, nror) — appended AFTER the
    swap sequencing text."""
    L = []
    o = L.append
    o(f"""
// ---- WRITEBACK strobes (swap counter domain; strictly before clr) ----
// slot0 <= D (nri = newReserveIn) at OK_CAP+2; slot1 <= nror (nro) at OK_CAP+3.
wire wb1 = (cnt == {sw.CNTW}'d{sw.OK_CAP + 2});
wire wb2 = (cnt == {sw.CNTW}'d{sw.OK_CAP + 3});
wire eff_we = we || wb1 || wb2;
wire [{AW-1}:0] eff_waddr = wb1 ? {AW}'d0 : (wb2 ? {AW}'d1 : waddr);
// per-lane write-value mux: lane k mixes only share-k values (public selects)
wire [{N-1}:0] wv0 = wb1 ? D0 : (wb2 ? nror0 : wd0);
wire [{N-1}:0] wv1 = wb1 ? D1 : (wb2 ? nror1 : wd1);

// ---- lane 0 writes ----
always @(posedge clk) begin""")
    for i in range(D):
        o(f"    if (eff_we && eff_waddr == {AW}'d{i}) m0_{i} <= wv0;")
    o("""end
// ---- lane 1 writes ----
always @(posedge clk) begin""")
    for i in range(D):
        o(f"    if (eff_we && eff_waddr == {AW}'d{i}) m1_{i} <= wv1;")
    o("end")
    return "\n".join(L)


def build(modname, leaf, comment, rndreuse=False, recomb=False, sharedbus=False):
    text = sw.header(modname, leaf, comment, recomb=recomb)
    # ---- port surgery: go -> tx_go + storage ports; ri/ro -> wdata/rdata ----
    text = rep1(text,
                "(clk, rst, go, x, ri, ro, mo, r, s, aout, nri, nro, ok",
                "(clk, rst, tx_go, we, waddr, raddr, wdata, x, mo,\n"
                "                r, s, aout, nri, nro, ok, rdata")
    text = rep1(text,
                '(* matchi_type = "control" *) input go;',
                '(* matchi_type = "control" *) input tx_go;\n'
                '(* matchi_type = "control" *) input we;\n'
                f'(* matchi_type = "control" *) input [{AW-1}:0] waddr;\n'
                f'(* matchi_type = "control" *) input [{AW-1}:0] raddr;')
    text = rep1(text,
                f'(* matchi_type = "sharings_dense", matchi_active = "in_act" *) input [{2*N-1}:0] ri;',
                f'(* matchi_type = "sharings_dense", matchi_active = "a_act" *) input [{2*N-1}:0] wdata;')
    text = rep1(text,
                f'(* matchi_type = "sharings_dense", matchi_active = "in_act" *) input [{2*N-1}:0] ro;',
                f'(* matchi_type = "sharings_dense", matchi_active = "st_out_act" *) output [{2*N-1}:0] rdata;')
    # ---- load-source surgery: reserves come from storage, not ports ----
    text = rep1(text, "hri0 <= " + sw.share_concat('ri', 0) + ";", "hri0 <= rri0;")
    text = rep1(text, "hri1 <= " + sw.share_concat('ri', 1) + ";", "hri1 <= rri1;")
    text = rep1(text, "hro0 <= " + sw.share_concat('ro', 0) + ";", "hro0 <= rro0;")
    text = rep1(text, "hro1 <= " + sw.share_concat('ro', 1) + ";", "hro1 <= rro1;")
    text = rep1(text, "m_aa0 <= " + sw.share_concat('ro', 0) + ";", "m_aa0 <= rro0;")
    text = rep1(text, "m_aa1 <= " + sw.share_concat('ro', 1) + ";", "m_aa1 <= rro1;")
    # ---- insert storage + prefix FSM before the swap counter ----
    text = rep1(text, "// ---- global cycle counter",
                storage_early(sharedbus=sharedbus) +
                "\n\n// ---- global cycle counter")
    parts = [text,
             storage_late(),
             sw.outputs_body(),
             sw.mul_body(leaf),
             sw.add_body(leaf),
             sw.div_body(leaf, rndreuse=rndreuse),
             sw.cmp_body(leaf),
             sw.subro_body(leaf)]
    if recomb:
        parts.append("""
// BUG UNDER TEST: recombine the two shares of aout bit 0. leak0 holds
// aout[0]^aout[1] (both shares) -> the XOR gate is sensitive in ShareSet{0}
// AND ShareSet{1} -> MATCHI glitch leakage. Driven onto share 0 of leak_o.
(* keep = "yes" *) reg leak0, leak1;
always @(posedge clk) begin
    leak0 <= aout[0] ^ aout[1];
    leak1 <= 1'b0;
end
assign leak_o = {leak1, leak0};""")
    parts.append("\nendmodule\n")
    return "\n".join(parts)


def emit(fn, text, leaf, expect_count):
    n = text.count(leaf + " u_")
    assert n == expect_count, f"{fn}: leaf {leaf} x{n}, expected {expect_count}"
    with open(fn, "w") as f:
        f.write(text)
    print(f"wrote {fn}: {n} {leaf} instances")


