// MATCHI-passed 8-bit top (./top_add8_opini2.v). EXACT same
// per-bit dataflow:
//   b0_eff = b0 ^ sub (share 0 only); p = a ^ b_eff (share-wise);
//   g = OPINI2-AND(a, b_eff); t = OPINI2-AND(carry, p);
//   carry[i+1] = g ^ t; sum = p ^ carry; carry[0] = {share1=0, share0=sub}.
// 512 assumed-OPINI gadget leaves (2 per bit; the byte-identical O-PINI2
