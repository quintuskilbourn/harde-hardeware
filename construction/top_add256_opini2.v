// per-instance indexing as the 8-bit top. Every XOR is strictly share-local.
//
// Unrolled (no generate loop) so each gadget instance is textually explicit.
// Generated by gen_add256.py; do not hand-edit — regenerate instead.
//
// Dense sharing layout (share index fastest): port[2i] = share0 of bit i,
// port[2i+1] = share1 of bit i.
//
// Schedule (cycle 0 = go), scaled from the 8-bit top by E = 2N+2 = 514
// back-to-back overlapped executions: b sensitive [0,513], a sensitive
// [1,514] (the HPC2 inb@0/ina@1 stagger). Randoms fresh per cycle over the
// whole sensitive-execution span (r [0,1027], s [1,1028] — covers the
// carry ripple tail: bit-i propagate has sensitive executions up to
// ~cycle 513+2i). Outputs windowed [0,1030].
